package lotto.model.enumeration;

import java.util.Arrays;
import java.util.Map;
import java.util.Optional;
import java.util.function.Function;
import java.util.stream.Collectors;

public enum Rank {
    FIRST(6, 2000000000),
    SECOND(5, 1500000),
    THIRD(4, 50000),
    FOURTH(3, 5000);

    private static final Map<Integer, Rank> RANK_MAP = Arrays.stream(Rank.values())
            .collect(Collectors.toMap(rank -> rank.countOfMatch, Function.identity()));

    private int countOfMatch;
    private int winningMoney;

    Rank(int countOfMatch, int winningMoney) {
        this.countOfMatch = countOfMatch;
        this.winningMoney = winningMoney;
    }

    public int getCountOfMatch() {
        return countOfMatch;
    }

    public int getWinningMoney() {
        return winningMoney;
    }

    public static Optional<Rank> valueOf(Integer countOfMatch) {
        return Optional.ofNullable(RANK_MAP.get(countOfMatch));
    }
}
