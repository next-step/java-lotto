package util;

public class Calculator {
    
    public static int plus(int leftNum, int rightNum) {
        return leftNum + rightNum;
    }

    public static int minus(int leftNum, int rightNum) {
        return leftNum - rightNum;
    }

    public static int multiply(int leftNum, int rightNum) {
        return leftNum * rightNum;
    }

    public static int division(int leftNum, int rightNum) {
        return leftNum / rightNum;
    }
}
