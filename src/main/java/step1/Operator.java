package step1;

public class Operator {
    public static int plus(int firstNum, int secondNum) {
        return firstNum + secondNum;
    }

    public static int minus(int firstNum, int secondNum) {
        return firstNum - secondNum;
    }

    public static int times(int firstNum, int secondNum) {
        return firstNum * secondNum;
    }

    public static int division(int firstNum, int secondNum) {
        return firstNum / secondNum;
    }
}

